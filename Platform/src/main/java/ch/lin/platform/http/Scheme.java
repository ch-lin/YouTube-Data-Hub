package ch.lin.platform.http;

/**
 * Represents the protocol scheme for an HTTP connection.
 */
public enum Scheme {
    /**
     * The Hypertext Transfer Protocol.
     */
    HTTP("http"),
    /**
     * The Hypertext Transfer Protocol Secure.
     */
    HTTPS("https");

    /**
     * The lowercase string representation of the scheme.
     */
    private final String value;

    /**
     * Private constructor to initialize the enum constant with its string
     * value.
     *
     * @param value The lowercase string representation of the scheme.
     */
    Scheme(String value) {
        this.value = value;
    }

    /**
     * Returns the string representation of the scheme (e.g., "http").
     *
     * @return The lowercase string value of the scheme.
     */
    @Override
    public String toString() {
        return this.value;
    }
}
