package ch.lin.authentication.service.backend.api.app.service;

import java.security.KeyPair;
import java.security.KeyPairGenerator;
import java.security.NoSuchAlgorithmException;
import java.security.interfaces.RSAPublicKey;
import java.util.Map;

import static org.assertj.core.api.Assertions.assertThat;
import org.junit.jupiter.api.Test;
import org.junit.jupiter.api.extension.ExtendWith;
import org.mockito.junit.jupiter.MockitoExtension;

@ExtendWith(MockitoExtension.class)
class JwkSetServiceTest {

    @Test
    void getJwkSet_ShouldReturnValidMap() throws NoSuchAlgorithmException {
        // Arrange
        KeyPairGenerator keyPairGenerator = KeyPairGenerator.getInstance("RSA");
        keyPairGenerator.initialize(2048);
        KeyPair keyPair = keyPairGenerator.generateKeyPair();
        RSAPublicKey publicKey = (RSAPublicKey) keyPair.getPublic();

        JwkSetService jwkSetService = new JwkSetService(publicKey);

        // Act
        Map<String, Object> jwkSetMap = jwkSetService.getJwkSet();

        // Assert
        assertThat(jwkSetMap).isNotNull();
        assertThat(jwkSetMap).containsKey("keys");
    }
}
